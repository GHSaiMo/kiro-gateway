def test_encrypted_write_preserves_mode_and_round_trips_without_plaintext(tmp_path):
    """
    What it does: Rewrites one encrypted account atomically with a fixed nonce.
    Purpose: Preserve Cockpit encryption, file permissions, and account data on refresh.
    """
    account_path = _write_fixture(tmp_path)
    account_path.chmod(0o600)
    updated_payload = dict(_FIXTURE_PAYLOAD, access_token="rotated_access")

    with (
        patch("kiro.cockpit_storage.secrets.token_bytes", return_value=b"n" * 12),
        patch("kiro.cockpit_storage.time.time", return_value=1784165000),
    ):
        write_account_document(account_path, updated_payload, encrypted=True)

    raw_text = account_path.read_text(encoding="utf-8")
    envelope = json.loads(raw_text)
    assert "rotated_access" not in raw_text
    assert envelope["version"] == 1
    assert envelope["kind"] == "kiro"
    assert envelope["algorithm"] == COCKPIT_SECURE_ALGORITHM
    assert envelope["key_id"] == COCKPIT_SECURE_KEY_ID
    assert envelope["encrypted_at"] == 1784165000
    assert account_path.stat().st_mode & 0o777 == 0o600
    assert read_account_document(account_path).payload == updated_payload
    assert not list(account_path.parent.glob(f".{account_path.name}.*.tmp"))


def test_plaintext_write_remains_plaintext_and_round_trips(tmp_path):
    """
    What it does: Writes a legacy plaintext account document.
    Purpose: Avoid forcing encryption on non-Cockpit credential files.
    """
    account_path = tmp_path / "credentials.json"

    write_account_document(account_path, _FIXTURE_PAYLOAD, encrypted=False)

    assert "fixture_access" in account_path.read_text(encoding="utf-8")
    document = read_account_document(account_path)
    assert document.payload == _FIXTURE_PAYLOAD
    assert document.encrypted is False


def test_failed_encrypted_write_does_not_replace_existing_document(tmp_path):
    """
    What it does: Removes the key immediately before an encrypted rewrite.
    Purpose: Ensure a storage failure cannot truncate the last usable account snapshot.
    """
    account_path = _write_fixture(tmp_path)
    original_content = account_path.read_text(encoding="utf-8")
    (account_path.parent.parent / "secure-account-storage.key").unlink()

    with pytest.raises(CockpitStorageError, match="解密密钥不存在"):
        write_account_document(account_path, _FIXTURE_PAYLOAD, encrypted=True)

    assert account_path.read_text(encoding="utf-8") == original_content
