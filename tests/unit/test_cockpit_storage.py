@pytest.mark.parametrize(
    ("field", "value", "expected_message"),
    [
        ("version", 2, "版本"),
        ("version", True, "版本"),
        ("kind", "codex", "不是 Kiro"),
        ("algorithm", "AES-128-GCM", "加密算法"),
        ("key_id", "unknown-key", "密钥类型"),
        ("nonce", "not-base64!", "nonce 不是有效 Base64"),
        ("nonce", base64.b64encode(b"short").decode("ascii"), "nonce 长度无效"),
        ("ciphertext", "not-base64!", "ciphertext 不是有效 Base64"),
        ("ciphertext", base64.b64encode(b"short").decode("ascii"), "密文过短"),
    ],
)
def test_rejects_unsupported_or_malformed_envelope_fields(
    tmp_path,
    field,
    value,
    expected_message,
):
    """
    What it does: Rejects malformed protocol metadata before decryption.
    Purpose: Fail safely and explain exactly which Cockpit field is incompatible.
    """
    account_path = _write_fixture(tmp_path)
    envelope = _fixture_envelope()
    envelope[field] = value
    account_path.write_text(json.dumps(envelope), encoding="utf-8")

    with pytest.raises(CockpitStorageError, match=expected_message):
        read_account_document(account_path)


def test_rejects_envelope_with_missing_required_field(tmp_path):
    """
    What it does: Rejects a partial envelope instead of treating it as plaintext.
    Purpose: Avoid misleading missing-token errors for damaged encrypted files.
    """
    account_path = _write_fixture(tmp_path)
    envelope = _fixture_envelope()
    del envelope["kind"]
    account_path.write_text(json.dumps(envelope), encoding="utf-8")

    with pytest.raises(CockpitStorageError, match="缺少有效的 kind"):
        read_account_document(account_path)


